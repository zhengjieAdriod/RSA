    /**
     * 签名(对报文进行摘要,并对摘要进行私钥加密,进而生成签名)
     *
     * @param data 被签名的数据
     * @param priK 私钥
     * @return 数字签名
     * @throws Exception
     */
    public static byte[] sign(byte[] data, PrivateKey priK) throws Exception {
        Signature sig = Signature.getInstance(SIGNATURE_ALGORITHM_SHA1);//MD5withRSA(MD5摘要算法)
        sig.initSign(priK);
        sig.update(data);
        return sig.sign();
    }

    /**
     * 验证签名
     *
     * @param data 被签名的数据
     * @param sign 签名
     * @param pubK 公钥
     * @return 是否验证通过
     * @throws Exception
     */
    public static boolean verify(byte[] data, byte[] sign, PublicKey pubK) throws Exception {
        Signature sig = Signature.getInstance(SIGNATURE_ALGORITHM_SHA1);//MD5withRSA(MD5摘要算法)
        sig.initVerify(pubK);
        sig.update(data);
        return sig.verify(sign);
    }

    /**
     * 10进制转bcd
     *
     * @param str 10进制数字 String.valueOf(int number);将10进制数字转成字符串传入此参数
     * @return bcd码
     */
    public static String DecimaltoBcd(String str) {
        String b_num = "";
        for (int i = 0; i < str.length(); i++) {

            String b = Integer.toBinaryString(Integer.parseInt(str.valueOf(str.charAt(i))));

            int b_len = 4 - b.length();

            for (int j = 0; j < b_len; j++) {
                b = "0" + b;
            }
            b_num += b;
        }

        return b_num;
    }


    /**
     * 16进制转bcd
     * 将16进制转成10进制，再将10进制转成bcd
     *
     * @param hex 16进制数字String.valueOf(int number);这里忽略16进制的前缀0x，只转后面的数字为字符串类型，将16进制数字转成字符串传入此参数
     * @return bcd码
     */
    public static String HextoBcd(String hex) {

        int decimal = Integer.parseInt(hex, 16);

        String bcd = DecimaltoBcd(String.valueOf(decimal));

        return bcd;
    }
